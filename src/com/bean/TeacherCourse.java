public class TeacherCourse{
    private String id;
    private String date;
    private String tab;
    private String lab_name;
    private String lab_name1;
    private String lab_address;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTab() {
        return tab;
    }

    public void setTab(String tab) {
        this.tab = tab;
    }

    public String getLab_name() {
        return lab_name;
    }

    public void setLab_name(String lab_name) {
        this.lab_name = lab_name;
    }

    public String getLab_name1() {
        return lab_name1;
    }

    public void setLab_name1(String lab_name1) {
        this.lab_name1 = lab_name1;
    }

    public String getLab_address() {
        return lab_address;
    }

    public void setLab_address(String lab_address) {
        this.lab_address = lab_address;
    }
}
